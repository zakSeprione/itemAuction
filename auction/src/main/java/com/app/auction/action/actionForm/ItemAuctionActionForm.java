package com.app.auction.action.actionForm;

public class ItemAuctionActionForm {
	
	private String itemName;
	private String itemDescription;
	private String itemPrice;
	private String itemDateClosed;
	private String itemStartingPrice;
	
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemDescription() {
		return itemDescription;
	}
	public void setItemDescription(String itemDescription) {
		this.itemDescription = itemDescription;
	}
	public String getItemPrice() {
		return itemPrice;
	}
	public void setItemPrice(String itemPrice) {
		this.itemPrice = itemPrice;
	}
	public String getItemDateClosed() {
		return itemDateClosed;
	}
	public void setItemDateClosed(String itemDateClosed) {
		this.itemDateClosed = itemDateClosed;
	}
	public String getItemStartingPrice() {
		return itemStartingPrice;
	}
	public void setItemStartingPrice(String itemStartingPrice) {
		this.itemStartingPrice = itemStartingPrice;
	}
	
	
	
	
}
